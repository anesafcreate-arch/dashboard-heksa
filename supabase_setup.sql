create table if not exists public.jadwal_onsite (
  id bigint generated by default as identity primary key,
  tanggal_onsite date not null,
  pelanggan text not null,
  lokasi text not null,
  teknisi text null,
  jenis_layanan text null,
  status text not null default 'TERJADWAL'
    check (status in ('TERJADWAL', 'PROSES', 'SELESAI', 'BATAL')),
  catatan text null,
  created_by uuid null default auth.uid() references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

